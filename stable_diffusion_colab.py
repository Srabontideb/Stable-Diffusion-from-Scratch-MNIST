"""---
## Part 1 — Noise Schedule

The **β schedule** controls how quickly noise is added. We use the linear schedule from Ho et al. (2020):

$$\beta_1 = 10^{-4} \;\to\; \beta_T = 0.02 \quad \text{(linearly spaced)}$$

From β we derive $\bar{\alpha}_t = \prod_{s=1}^{t}(1-\beta_s)$, which lets us sample *any* noisy image directly:

$$x_t = \sqrt{\bar{\alpha}_t}\, x_0 + \sqrt{1-\bar{\alpha}_t}\, \varepsilon$$
"""

class NoiseSchedule:
    """
    Linear β noise schedule.

    Key quantities:
      betas        β_t  — noise variance added at step t
      alphas       α_t = 1 - β_t
      alpha_bars   ᾱ_t = ∏ α_s  (cumulative product)
    """
    def __init__(self, T: int, beta_start=1e-4, beta_end=0.02):
        self.T = T
        self.betas            = torch.linspace(beta_start, beta_end, T).to(DEVICE)
        self.alphas           = 1.0 - self.betas
        self.alpha_bars       = torch.cumprod(self.alphas, dim=0)
        self.sqrt_alpha_bars  = torch.sqrt(self.alpha_bars)
        self.sqrt_one_minus_ab = torch.sqrt(1.0 - self.alpha_bars)
        self.sqrt_recip_alpha  = torch.sqrt(1.0 / self.alphas)

    def q_sample(self, x0: torch.Tensor, t: torch.Tensor):
        """
        Forward process — add noise to x0 at timestep t.
        Returns (x_noisy, noise).
        """
        noise       = torch.randn_like(x0)
        sqrt_ab     = self.sqrt_alpha_bars[t].view(-1, 1, 1, 1)
        sqrt_one_ab = self.sqrt_one_minus_ab[t].view(-1, 1, 1, 1)
        x_noisy     = sqrt_ab * x0 + sqrt_one_ab * noise
        return x_noisy, noise

schedule = NoiseSchedule(T)
