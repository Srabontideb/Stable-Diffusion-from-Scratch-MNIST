print(f"   β range  : {schedule.betas[0].item():.5f} → {schedule.betas[-1].item():.4f}")
print(f"   ᾱ range  : {schedule.alpha_bars[0].item():.4f} → {schedule.alpha_bars[-1].item():.6f}")

"""---
## Part 2 — U-Net Architecture

The denoiser is a **U-Net** with three key ingredients:

1. **Sinusoidal Time Embedding** — encodes scalar `t` into a rich vector (same idea as Transformer positional encoding)
2. **ResBlock** — convolutional block that *injects* the time embedding so the network knows how noisy the input is
3. **Self-Attention** — at the bottleneck, every pixel can attend to every other pixel for global coherence
4. **Skip connections** — encoder features are concatenated into the decoder, preserving fine spatial detail
"""

# ── 2a. Sinusoidal Time Embedding ──────────────────────────────────────────
class SinusoidalTimeEmbedding(nn.Module):
    """
    Encodes scalar timestep t → dense vector.
    Uses sin/cos at different frequencies, then passes through MLP.

    Why needed: the U-Net must behave differently at t=1 (barely noisy)
    vs t=299 (nearly pure noise).
    """
    def __init__(self, dim: int):
        super().__init__()
        self.dim = dim
        self.mlp = nn.Sequential(
            nn.Linear(dim, dim * 4),
            nn.SiLU(),            # Swish: smoother than ReLU
            nn.Linear(dim * 4, dim),
        )

    def forward(self, t: torch.Tensor) -> torch.Tensor:
        half  = self.dim // 2
        freqs = torch.exp(
            -math.log(10000) * torch.arange(half, device=t.device) / (half - 1)
        )
        args  = t[:, None].float() * freqs[None, :]
        emb   = torch.cat([torch.sin(args), torch.cos(args)], dim=-1)
        return self.mlp(emb)

print("✅ SinusoidalTimeEmbedding defined")

# Quick test
