# ── 2b. ResBlock ───────────────────────────────────────────────────────────
class ResBlock(nn.Module):
    """
    Residual block with time injection.

    Flow: x → Norm → Conv → SiLU → (+t_emb) → Norm → Conv → (+skip)

    Time embedding is projected to (out_ch,) and broadcast-added
    over the spatial dimensions, giving direct control over noise level.
    """
    def __init__(self, in_ch: int, out_ch: int, time_dim: int):
        super().__init__()
        self.norm1     = nn.GroupNorm(8, in_ch)
        self.conv1     = nn.Conv2d(in_ch, out_ch, 3, padding=1)
        self.norm2     = nn.GroupNorm(8, out_ch)
        self.conv2     = nn.Conv2d(out_ch, out_ch, 3, padding=1)
        self.time_proj = nn.Linear(time_dim, out_ch)     # ← inject t here
        self.skip      = nn.Conv2d(in_ch, out_ch, 1) if in_ch != out_ch else nn.Identity()

    def forward(self, x: torch.Tensor, t_emb: torch.Tensor) -> torch.Tensor:
        h = self.conv1(F.silu(self.norm1(x)))
        h = h + self.time_proj(F.silu(t_emb))[:, :, None, None]  # broadcast over H,W
        h = self.conv2(F.silu(self.norm2(h)))
        return h + self.skip(x)

print("✅ ResBlock defined")

# ── 2c. Self-Attention ─────────────────────────────────────────────────────
class SelfAttention(nn.Module):
    """
    Spatial self-attention at the bottleneck (7×7 feature map).
    Lets every spatial position attend to every other — essential
    for global coherence (e.g. consistent digit shape).
    """
    def __init__(self, channels: int):
        super().__init__()
        self.norm = nn.GroupNorm(8, channels)
        self.qkv  = nn.Conv2d(channels, channels * 3, 1)
        self.proj = nn.Conv2d(channels, channels, 1)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        B, C, H, W = x.shape
        h   = self.norm(x)
        qkv = self.qkv(h).reshape(B, 3, C, H * W).permute(1, 0, 2, 3)
        q, k, v = qkv[0], qkv[1], qkv[2]
        # Scaled dot-product attention
        attn = torch.softmax(q.transpose(-1,-2) @ k * (C ** -0.5), dim=-1)
        out  = (attn @ v.transpose(-1,-2)).transpose(-1,-2)
        out  = out.reshape(B, C, H, W)
        return x + self.proj(out)   # residual

print("✅ SelfAttention defined")

# ── 2d. Full U-Net ─────────────────────────────────────────────────────────
class UNet(nn.Module):
    """
    Mini U-Net denoiser for 28×28 images.

    Input  : (B, 1, 28, 28) noisy image  +  (B,) timestep t
    Output : (B, 1, 28, 28) predicted noise ε̂

    Spatial resolution:
      Encoder  :  28 → 14 → 7
      Bottleneck:  7  (with self-attention)
      Decoder  :   7 → 14 → 28  (+ skip connections from encoder)
    """
    def __init__(self, img_channels=1, base_dim=64, time_dim=128):
        super().__init__()
        self.time_mlp = SinusoidalTimeEmbedding(time_dim)

        # ── Encoder ──────────────────────────────────────────────────────
        self.enc_in   = nn.Conv2d(img_channels, base_dim, 3, padding=1)
        self.enc_res1 = ResBlock(base_dim,      base_dim,     time_dim)
        self.down1    = nn.Conv2d(base_dim,     base_dim * 2, 4, 2, 1)   # 28 → 14
        self.enc_res2 = ResBlock(base_dim * 2,  base_dim * 2, time_dim)
        self.down2    = nn.Conv2d(base_dim * 2, base_dim * 4, 4, 2, 1)   # 14 → 7

        # ── Bottleneck ────────────────────────────────────────────────────
        self.bot_res1 = ResBlock(base_dim * 4, base_dim * 4, time_dim)
        self.bot_attn = SelfAttention(base_dim * 4)
        self.bot_res2 = ResBlock(base_dim * 4, base_dim * 4, time_dim)

        # ── Decoder  (note: *2 channels due to skip concat) ───────────────
        self.up2      = nn.ConvTranspose2d(base_dim * 4, base_dim * 2, 4, 2, 1)  # 7 → 14
        self.dec_res2 = ResBlock(base_dim * 4, base_dim * 2, time_dim)  # *4: skip
        self.up1      = nn.ConvTranspose2d(base_dim * 2, base_dim,     4, 2, 1)  # 14 → 28
        self.dec_res1 = ResBlock(base_dim * 2, base_dim,     time_dim)  # *2: skip

        # ── Output ────────────────────────────────────────────────────────
        self.out_norm = nn.GroupNorm(8, base_dim)
        self.out_conv = nn.Conv2d(base_dim, img_channels, 1)

    def forward(self, x: torch.Tensor, t: torch.Tensor) -> torch.Tensor:
        t_emb = self.time_mlp(t)                           # (B, time_dim)

        # Encoder
        x1 = self.enc_res1(self.enc_in(x),  t_emb)        # (B, 64,  28, 28)
        x2 = self.enc_res2(self.down1(x1),  t_emb)        # (B, 128, 14, 14)
        x3 = self.down2(x2)                                # (B, 256,  7,  7)

        # Bottleneck
        x3 = self.bot_res1(x3, t_emb)
        x3 = self.bot_attn(x3)
        x3 = self.bot_res2(x3, t_emb)

        # Decoder (concatenate skip connections)
        x  = self.dec_res2(torch.cat([self.up2(x3), x2], dim=1), t_emb)
        x  = self.dec_res1(torch.cat([self.up1(x),  x1], dim=1), t_emb)

        return self.out_conv(F.silu(self.out_norm(x)))


