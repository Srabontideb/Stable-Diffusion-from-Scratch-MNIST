"""---
## Part 5 — Training Loop

**DDPM training algorithm** (Algorithm 1, Ho et al. 2020) — surprisingly simple:

```
for each batch:
  1. Sample random t ~ Uniform(0, T)
  2. Sample ε ~ N(0, I)
  3. Compute x_t = √ᾱ_t · x₀ + √(1-ᾱ_t) · ε       ← closed form!
  4. Predict ε̂ = U-Net(x_t, t)
  5. Loss = MSE(ε, ε̂)
  6. Backprop + Adam step
```
"""

def train(model, schedule, loader):
    optimizer  = torch.optim.Adam(model.parameters(), lr=LR)
    epoch_losses = []

    for epoch in range(EPOCHS):
        running_loss = 0.0
        pbar = tqdm(loader, desc=f"Epoch {epoch+1}/{EPOCHS}", leave=True)

        for images, _ in pbar:
            images = images.to(DEVICE)                         # (B,1,28,28)
            B      = images.shape[0]

            # Step 1: random timestep per image
            t = torch.randint(0, T, (B,), device=DEVICE)

            # Steps 2 & 3: add noise (closed form — no loop needed!)
            x_noisy, noise = schedule.q_sample(images, t)

            # Step 4: predict the noise
            pred_noise = model(x_noisy, t)

            # Step 5: MSE loss
            loss = F.mse_loss(pred_noise, noise)

            # Step 6: optimise
            optimizer.zero_grad()
            loss.backward()
            optimizer.step()

            running_loss += loss.item()
            pbar.set_postfix(loss=f"{loss.item():.4f}")

        avg = running_loss / len(loader)
        epoch_losses.append(avg)
        print(f"  Epoch {epoch+1:02d}/{EPOCHS} — avg loss: {avg:.4f}")

    return epoch_losses


print("Starting training...\n")
