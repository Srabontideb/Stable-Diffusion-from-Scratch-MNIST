"""---
## Part 6 — Sampling (Reverse Process)

**DDPM sampling algorithm** (Algorithm 2, Ho et al. 2020):

Start from $x_T \sim \mathcal{N}(0, I)$, then iterate **T → 1**:

$$x_{t-1} = \frac{1}{\sqrt{\alpha_t}} \left( x_t - \frac{\beta_t}{\sqrt{1-\bar{\alpha}_t}} \hat{\varepsilon}_\theta(x_t, t) \right) + \sqrt{\beta_t}\, z \qquad z \sim \mathcal{N}(0, I)$$

> This takes ~300 steps. After training more epochs (30+), digits become much sharper.
"""

@torch.no_grad()
def sample_ddpm(model, schedule, n_samples=16):
    """Generate images by running the reverse diffusion process T → 0."""
    model.eval()
    # Start from pure Gaussian noise
    x = torch.randn(n_samples, CHANNELS, IMG_SIZE, IMG_SIZE, device=DEVICE)

    for t_idx in tqdm(reversed(range(T)), desc="Sampling", total=T, leave=True):
        t = torch.full((n_samples,), t_idx, device=DEVICE, dtype=torch.long)

        pred_noise   = model(x, t)
        alpha        = schedule.alphas[t_idx]
        beta         = schedule.betas[t_idx]
        sqrt_recip   = schedule.sqrt_recip_alpha[t_idx]
        sqrt_one_ab  = schedule.sqrt_one_minus_ab[t_idx]

        # Compute posterior mean
        x = sqrt_recip * (x - (beta / sqrt_one_ab) * pred_noise)

        # Add noise for all steps except the last
        if t_idx > 0:
            x = x + torch.sqrt(beta) * torch.randn_like(x)

    model.train()
    return x.cpu()


